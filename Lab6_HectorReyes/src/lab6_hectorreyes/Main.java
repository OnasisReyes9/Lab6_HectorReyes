    private void jMIT_guardarCancionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMIT_guardarCancionActionPerformed
        // TODO add your handling code here:
        if (archivoActual != null) {
            DefaultTableModel model = (DefaultTableModel) jTbl_canciones.getModel();
            String acum = "";
            for (int i = 0; i < model.getRowCount(); i++) {
                for (int j = 0; j < model.getColumnCount(); j++) {
                    acum += model.getValueAt(i, j).toString() + ";";
                }
                acum += "\n";
            }
            if (acum.length() >= 1) {
                acum = acum.substring(0, acum.length() - 1);
            }
            // acum = acum.replace(";", "|");
            FileWriter fw;
            try {
                fw = new FileWriter(archivoActual);
                try (BufferedWriter bw = new BufferedWriter(fw)) {
                    bw.write(acum);
                    bw.flush();
                    JOptionPane.showMessageDialog(this, "Se han guardado los cambios.");
                }
                fw.close();
            } catch (IOException ex) {
                Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            jMI_CrearPlayListActionPerformed(evt);
        }
    }//GEN-LAST:event_jMIT_guardarCancionActionPerformed

    private void jMI_buscarPlayListActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMI_buscarPlayListActionPerformed
        // TODO add your handling code here:
        File fichero = null;
        FileReader fr = null;
        BufferedReader br = null;
        cleanJtable();
        DefaultTableModel model = (DefaultTableModel) jTbl_canciones.getModel();
        try {
            JFileChooser jfc = new JFileChooser("./");
            FileNameExtensionFilter filtro
                    = new FileNameExtensionFilter(
                            "Archivos de Texto", "txt");
            jfc.setFileFilter(filtro);
            int seleccion = jfc.showOpenDialog(this);
            if (seleccion == JFileChooser.APPROVE_OPTION) {
                fichero = jfc.getSelectedFile();
                archivoActual = jfc.getSelectedFile();
                fr = new FileReader(fichero);
                br = new BufferedReader(fr);
                String linea;
                cleanJtable();
                while ((linea = br.readLine()) != null) {
                    //  linea = linea.replace(";", "|");
                    String[] filas = linea.split(";");
                    model.addRow(filas);
                }
                br.close();
                fr.close();
            } //fin if
        } catch (Exception e) {
        }
        try {
            br.close();
            fr.close();
        } catch (IOException ex) {
        }
        jTbl_canciones.setModel(model);
    }//GEN-LAST:event_jMI_buscarPlayListActionPerformed

