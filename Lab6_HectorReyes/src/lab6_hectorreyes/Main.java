    private void jMI_CrearPlayListActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMI_CrearPlayListActionPerformed
        // TODO add your handling code here:
        if (jTbl_canciones.getSelectedRow() >= 0) {
            // String nombrePlayList = JOptionPane.showInputDialog("Ingrese el nombre de la PlayList");
            JFileChooser jfc = new JFileChooser();
            FileNameExtensionFilter filtro = new FileNameExtensionFilter("Archivos de Texto", "txt");//"txt"
            jfc.setFileFilter(filtro);
            int seleccion = jfc.showSaveDialog(this);
            FileWriter fw = null;
            BufferedWriter bw = null;
            if (seleccion == JFileChooser.APPROVE_OPTION) {
                try {
                    File file = null;
                    if (jfc.getFileFilter().getDescription().equals("Archivos de Texto")) {
                        file = new File(jfc.getSelectedFile().getPath() + ".txt");
                    } else {
                        file = jfc.getSelectedFile();
                    }
                    archivoActual = file;
                    PlayList playList = new PlayList(jfc.getSelectedFile().getPath(), jfc.getSelectedFile().getPath());
                    playListActual = playList;
                    fw = new FileWriter(file);
                    bw = new BufferedWriter(fw);
                    DefaultTableModel model = (DefaultTableModel) jTbl_canciones.getModel();
                    String acum = "";
                    for (int i = 0; i < model.getRowCount(); i++) {
                        for (int j = 0; j < model.getColumnCount(); j++) {
                            acum += model.getValueAt(i, j) + ";";
                        }//fin for
                        acum += "\n";
                    }//fin for
                    //acum = acum.substring(0, acum.length()-1);
                    //acum = acum.replace(";", "|");
                    bw.write(acum);
                    bw.flush();
                    bw.close();
                    fw.close();
                    JOptionPane.showMessageDialog(this,
                            "PlayList Creada");
                } catch (IOException io) {
                    JOptionPane.showMessageDialog(this,
                            "Ha ocurrido un error y no se ha podido crear la PlayList");
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "No hay una canción seleccionada para la creación de una playList.");
        }
    }//GEN-LAST:event_jMI_CrearPlayListActionPerformed

    private void jMI_modificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMI_modificarActionPerformed
        // TODO add your handling code here:

        DefaultTableModel model = (DefaultTableModel) jTbl_canciones.getModel();
        if (jTbl_canciones.getSelectedRow() >= 0) {
            int col = -1;
            while (col < 0 || col > 4) {
                col = Integer.parseInt(JOptionPane.showInputDialog("Ingrese la columna de la fila seleccionada  [0,1,2,3,4,5]: "));
            }
            switch (col) {
                case 0:
                    String nombreCancion = JOptionPane.showInputDialog("Ingrese nuevo nombre de la cancion");
                    break;
                case 1:
                    int puntuacion = -1;
                    while (puntuacion < 0 || puntuacion > 10) {
                        puntuacion = Integer.parseInt(JOptionPane.showInputDialog("Ingrese nueva puntiación: [1-10]"));
                    }
                    String puntuacionCancion = puntuacion + "";
                    break;
                case 2:
                    String año = JOptionPane.showInputDialog("Ingrese nuevo año");
                    break;
                case 3:
                    String nombreArtista = JOptionPane.showInputDialog("Nuevo nombre del artista");
                    break;
                case 4:
                    String nombreAlbum = JOptionPane.showInputDialog("Nuevo nombre del álbum");
                    break;
                    
                    
                    
            }
            
            
            
        } else {
            JOptionPane.showMessageDialog(this, "No hay canción seleccionada");
        }
    }//GEN-LAST:event_jMI_modificarActionPerformed

